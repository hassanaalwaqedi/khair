import '../../domain/entities/event.dart';

class EventModel extends Event {
  const EventModel({
    required super.id,
    required super.organizerId,
    required super.title,
    super.description,
    required super.eventType,
    super.language,
    super.country,
    super.city,
    super.address,
    super.fullAddress,
    super.latitude,
    super.longitude,
    super.meetingUrl,
    super.meetingPlatform,
    required super.startDate,
    super.endDate,
    super.imageUrl,
    super.capacity,
    super.reservedCount,
    required super.status,
    super.rejectionReason,
    super.organizerName,
    super.isOnline,
    super.onlineLink,
    super.joinInstructions,
    super.joinLinkVisibleBeforeMinutes,
    super.isUserJoined,
    super.isLinkUnlocked,
    required super.createdAt,
    required super.updatedAt,
    super.ticketPrice,
    super.currency,
  });

  factory EventModel.fromJson(Map<String, dynamic> json) {
    return EventModel(
      id: json['id'] as String,
      organizerId: json['organizer_id'] as String,
      title: json['title'] as String,
      description: json['description'] as String?,
      eventType: json['event_type'] as String,
      language: json['language'] as String?,
      country: json['country'] as String?,
      city: json['city'] as String?,
      address: json['address'] as String?,
      fullAddress: json['full_address'] as String?,
      latitude: (json['latitude'] as num?)?.toDouble(),
      longitude: (json['longitude'] as num?)?.toDouble(),
      meetingUrl: json['meeting_url'] as String?,
      meetingPlatform: json['meeting_platform'] as String?,
      startDate: DateTime.parse(json['start_date'] as String),
      endDate: json['end_date'] != null
          ? DateTime.parse(json['end_date'] as String)
          : null,
      imageUrl: json['image_url'] as String?,
      capacity: json['capacity'] as int?,
      reservedCount: json['reserved_count'] as int? ?? 0,
      status: json['status'] as String,
      rejectionReason: json['rejection_reason'] as String?,
      organizerName: json['organizer_name'] as String?,
      isOnline: json['is_online'] as bool? ?? false,
      onlineLink: json['online_link'] as String?,
      joinInstructions: json['join_instructions'] as String?,
      joinLinkVisibleBeforeMinutes:
          json['join_link_visible_before_minutes'] as int? ?? 15,
      isUserJoined: json['is_user_joined'] as bool? ?? false,
      isLinkUnlocked: json['is_link_unlocked'] as bool? ?? false,
      createdAt: DateTime.parse(json['created_at'] as String),
      updatedAt: DateTime.parse(json['updated_at'] as String),
      ticketPrice: (json['ticket_price'] as num?)?.toDouble(),
      currency: json['currency'] as String?,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'organizer_id': organizerId,
      'title': title,
      'description': description,
      'event_type': eventType,
      'language': language,
      'country': country,
      'city': city,
      'address': address,
      'full_address': fullAddress,
      'latitude': latitude,
      'longitude': longitude,
      'meeting_url': meetingUrl,
      'meeting_platform': meetingPlatform,
      'start_date': startDate.toIso8601String(),
      'end_date': endDate?.toIso8601String(),
      'image_url': imageUrl,
      'capacity': capacity,
      'reserved_count': reservedCount,
      'status': status,
      'rejection_reason': rejectionReason,
      'organizer_name': organizerName,
      'is_online': isOnline,
      'online_link': onlineLink,
      'join_instructions': joinInstructions,
      'join_link_visible_before_minutes': joinLinkVisibleBeforeMinutes,
      'is_user_joined': isUserJoined,
      'is_link_unlocked': isLinkUnlocked,
      'created_at': createdAt.toIso8601String(),
      'updated_at': updatedAt.toIso8601String(),
      'ticket_price': ticketPrice,
      'currency': currency,
    };
  }
}
